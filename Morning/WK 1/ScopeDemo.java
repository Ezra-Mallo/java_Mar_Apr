public class ScopeDemo
{

    

    public static void main(String[] args)
    {
        int monthDays = 30;
        System.out.println("Month Days is " + monthDays);
        display();
    }

    public static void display()
    {
        int monthDays = 31;
        System.out.println("Month Days is " + monthDays); 
    }
}